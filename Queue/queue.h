#include<iostream>
#include <cstdlib>
/******************************************/

struct Queue{
    char item[10];
    int FRONT,REAR;
};
/******************************************/


void initialise(Queue *Q){
    (*Q).REAR=-1;
    (*Q).FRONT=0;
}

/******************************************/


bool IsEmpty(Queue *Q){
    if(Q->REAR-Q->FRONT+1==0)
        return true;
    else
        return false;
}

/******************************************/

void EnQueue(Queue *Q,char x){
    if(Q->REAR==9){
        std::cout<<"Queue Overflow";
        exit(1);
    }
    else{
        Q->REAR++;
        Q->item[Q->REAR]=x;
    }
}

/******************************************/

char DeQueue(Queue *Q){
    if(Q->REAR-Q->FRONT+1==0){
        std::cout<<"Queue Underflow";
        exit(1);
    }
    else{
        char x=Q->item[Q->FRONT];
        Q->FRONT++;
        return x;
    }
}

